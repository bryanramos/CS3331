public class Member {
    public int ID = 0;
    public String name = "";

    // setters
    void setID(int id) {
        ID = id; 
    }
    void setName(String n) {
        name = n;
    }
    // getters
    int getID() {
        return ID;
    }
    String getName() {
        return name;
    }
}